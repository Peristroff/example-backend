// User schema
const mongoose = require('mongoose');

const UserDetailsSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    surname: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    createdAt: {
        type: Date,
        default: Date.now
    }
},{
    collection: 'UserInfo'
})

mongoose.model('UserInfo', UserDetailsSchema);